# 머지된 settings.json 옆에 AC가 넣은 top-level 키 목록을 기록하는 사이드카.
# 다음 sync에서 "지난번엔 AC가 넣었지만 이번 소스엔 없는 키"(고아)를 식별해 제거하는 데 쓴다.
# merge_settings는 소스 키를 추가만 하므로, 이 매니페스트 없이는 소스에서 사라진 키가 영영 잔존한다.
def settings_manifest_path(target_path):
    return os.path.join(os.path.dirname(target_path), f".{os.path.basename(target_path)}.ac-keys")


def read_managed_keys(target_path):
    manifest_path = settings_manifest_path(target_path)
    if not os.path.exists(manifest_path):
        return []
    try:
        with open(manifest_path, "r", encoding="utf-8") as handle:
            parsed = json.loads(handle.read())
        return parsed if isinstance(parsed, list) else []
    except Exception:
        return []


def merge_settings(deploy, target_path):
    deploy_keys = list(deploy.keys())
    existing = read_json(target_path) if os.path.exists(target_path) else {}

    # 지난 sync에서 AC가 넣었지만 이번 소스엔 없는 top-level 키(고아)를 제거한다.
    for key in read_managed_keys(target_path):
        if key not in deploy_keys:
            existing.pop(key, None)

    merged = {**existing, **deploy}
    write_json(target_path, merged)
    with open(settings_manifest_path(target_path), "w", encoding="utf-8") as handle:
        handle.write(json.dumps(deploy_keys) + "\n")


def split_settings(deploy, target_path):
    if not os.path.exists(target_path):
        manifest_path = settings_manifest_path(target_path)
        if os.path.exists(manifest_path):
            remove_path(manifest_path)
        return False

    existing = read_json(target_path)
    # 현재 소스 키 + 지난 AC 관리 키(고아)를 모두 제거한다.
    keys_to_remove = set(deploy.keys()) | set(read_managed_keys(target_path))
    for key in keys_to_remove:
        existing.pop(key, None)

    manifest_path = settings_manifest_path(target_path)
    if os.path.exists(manifest_path):
        remove_path(manifest_path)

    if len(existing) == 0:
        remove_path(target_path)
    else:
        write_json(target_path, existing)
    return True


def verify_settings(deploy, target_path):
    if not os.path.exists(target_path):
        return False

    target = read_json(target_path)
    return all(stable_json(deploy[key]) == stable_json(target.get(key)) for key in deploy.keys())


# whole-file 타겟(codex hooks.json)용: 부분키 비교가 아니라 전체 일치를 본다.
def verify_json_exact(expected, target_path):
    if not os.path.exists(target_path):
        return False
    return stable_json(read_json(target_path)) == stable_json(expected)


def stable_json(value):
    return json.dumps(value, ensure_ascii=False)


def list_entries(directory):
    if not os.path.exists(directory):
        return []
    return sorted(os.scandir(directory), key=lambda e: e.name)


def compare_paths(left, right):
    if not os.path.exists(left) or not os.path.exists(right):
        return False

    left_is_dir = os.path.isdir(left)
    right_is_dir = os.path.isdir(right)
    if left_is_dir != right_is_dir:
        return False

    if left_is_dir:
        left_entries = list_entries(left)
        right_entries = list_entries(right)
        if len(left_entries) != len(right_entries):
            return False

        for i in range(len(left_entries)):
            if left_entries[i].name != right_entries[i].name:
                return False
            if not compare_paths(
                os.path.join(left, left_entries[i].name),
                os.path.join(right, right_entries[i].name),
            ):
                return False
        return True

    with open(left, "rb") as lf, open(right, "rb") as rf:
        return lf.read() == rf.read()


def deploy_root_files(target_dir, log):
    copied = 0
    for entry in list_entries(source_dir):
        if not entry.is_file():
            continue
        if entry.name in SOURCE_ONLY_ROOT_FILES:
            continue

        src = os.path.join(source_dir, entry.name)
        dest = os.path.join(target_dir, entry.name)
        copy_path(src, dest)
        log(f"  COPY  {entry.name}")
        copied += 1

    merge_settings(claude_settings_object(), os.path.join(target_dir, "settings.json"))
    log("  MERGE settings.json")
    copied += 1
    return copied


