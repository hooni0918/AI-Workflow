def trust_codex_hooks(target_dir, log=print, cwd=None):
    if cwd is None:
        cwd = repo_dir
    hooks_config = os.path.join(target_dir, "hooks.json")
    if not (os.path.exists(hooks_config) and os.path.isfile(hooks_config)):
        return 0

    response = run_codex_app_server(
        [{"method": "hooks/list", "params": {"cwds": [cwd]}}]
    )
    list_result = None
    for message in response:
        result = (message or {}).get("result") if isinstance(message, dict) else None
        if isinstance(result, dict) and isinstance(result.get("data"), list):
            list_result = result
            break

    hooks = []
    if list_result and isinstance(list_result.get("data"), list):
        for entry in list_result["data"]:
            hooks.extend(entry.get("hooks") or [])

    deployed_hooks = [
        hook
        for hook in hooks
        if os.path.abspath(hook.get("sourcePath") or "") == os.path.abspath(hooks_config)
    ]

    state = {}
    for hook in deployed_hooks:
        if not hook.get("key") or not hook.get("currentHash") or hook.get("isManaged"):
            continue
        state[hook["key"]] = {
            "enabled": True,
            "trusted_hash": hook["currentHash"],
        }

    if len(state) == 0:
        return 0

    run_codex_app_server(
        [
            {
                "method": "config/batchWrite",
                "params": {
                    "edits": [
                        {
                            "keyPath": "hooks.state",
                            "value": state,
                            "mergeStrategy": "upsert",
                        }
                    ],
                    "reloadUserConfig": True,
                },
            }
        ]
    )

    log(f"  TRUST hooks.state ({len(state)})")
    return len(state)


def resolve_codex_cli():
    explicit = os.environ.get("CODEX_CLI")
    if explicit and is_runnable_codex_cli(explicit):
        return explicit

    names = (
        ["codex.exe", "codex.cmd", "codex.bat", "codex"]
        if sys.platform == "win32"
        else ["codex"]
    )
    path_hit = find_on_path(names)
    if path_hit:
        return path_hit

    if sys.platform == "win32":
        candidates = [
            os.path.join(
                os.path.expanduser("~"), "AppData", "Local", "OpenAI", "Codex", "bin", "codex.exe"
            ),
            os.path.join(os.environ.get("LOCALAPPDATA", ""), "OpenAI", "Codex", "bin", "codex.exe"),
            *find_local_app_codex_bins(),
            *find_windows_app_codex_bins(),
        ]
        for candidate in candidates:
            if is_runnable_codex_cli(candidate):
                return candidate
        return None

    return None


def find_on_path(names):
    path_value = os.environ.get("PATH", "")
    dirs = [d for d in path_value.split(os.pathsep) if d]
    for directory in dirs:
        for name in names:
            candidate = os.path.join(directory, name)
            if is_runnable_codex_cli(candidate):
                return candidate
    return None


def find_local_app_codex_bins():
    roots = [
        os.path.join(os.path.expanduser("~"), "AppData", "Local", "OpenAI", "Codex", "bin"),
        os.path.join(os.environ.get("LOCALAPPDATA", ""), "OpenAI", "Codex", "bin"),
    ]
    candidates = []

    seen = []
    for root in roots:
        if root in seen:
            continue
        seen.append(root)
        if not root or not os.path.exists(root) or not os.path.isdir(root):
            continue

        for entry in os.listdir(root):
            entry_path = os.path.join(root, entry)
            try:
                if os.path.isdir(entry_path):
                    candidates.append(os.path.join(entry_path, "codex.exe"))
            except Exception:
                # Ignore stale package directories while scanning Desktop-managed bins.
                pass

    def mtime(path):
        try:
            return os.stat(path).st_mtime
        except Exception:
            return 0

    candidates.sort(key=mtime, reverse=True)
    return candidates


def find_windows_app_codex_bins():
    windows_apps = os.path.join(
        os.environ.get("ProgramFiles") or "C:\\Program Files", "WindowsApps"
    )
    if not (os.path.exists(windows_apps) and os.path.isdir(windows_apps)):
        return []

    try:
        result = []
        for entry in os.listdir(windows_apps):
            if entry.startswith("OpenAI.Codex_"):
                result.append(
                    os.path.join(windows_apps, entry, "app", "resources", "codex.exe")
                )
                result.append(os.path.join(windows_apps, entry, "app", "resources", "codex"))
        return result
    except Exception:
        return []


def is_executable_file(file):
    if not file:
        return False
    try:
        return os.path.exists(file) and os.path.isfile(file)
    except Exception:
        return False


def is_runnable_codex_cli(file):
    if not is_executable_file(file):
        return False

    try:
        result = subprocess.run(
            [file, "--version"],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
            stdin=subprocess.DEVNULL,
            timeout=5,
        )
    except Exception:
        return False

    return result.returncode == 0


def run_codex_app_server(requests, options=None):
    options = options or {}
    cwd = options.get("cwd") or repo_dir
    timeout_seconds = (options.get("timeoutMs") or 30000) / 1000.0

    codex_cli = resolve_codex_cli()
    if not codex_cli:
        raise RuntimeError(
            "Codex CLI not found. Install Codex Desktop/CLI, add codex to PATH, "
            "or set CODEX_CLI to the codex executable path."
        )

    import threading

    child = subprocess.Popen(
        [codex_cli, "app-server", "--listen", "stdio://"],
        cwd=cwd,
        stdin=subprocess.PIPE,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        encoding="utf-8",
    )

    results = []
    pending = {}
    next_id = [1]
    stderr_buf = []
    settled = {"done": False, "error": None}
    lock = threading.Lock()
    finished = threading.Event()

    def finish(error):
        with lock:
            if settled["done"]:
                return
            settled["done"] = True
            settled["error"] = error
        try:
            child.kill()
        except Exception:
            pass
        finished.set()

    def send(method, params):
        request_id = next_id[0]
        next_id[0] += 1
        pending[request_id] = len(results)
        results.append(None)
        child.stdin.write(json.dumps({"id": request_id, "method": method, "params": params}) + "\n")
        child.stdin.flush()

    def read_stdout():
        try:
            for line in child.stdout:
                for raw in line.replace("\r\n", "\n").split("\n"):
                    if not raw.strip():
                        continue
                    try:
                        message = json.loads(raw)
                    except Exception:
                        continue

                    msg_id = message.get("id")
                    if not msg_id or msg_id not in pending:
                        continue
                    index = pending.pop(msg_id)
                    results[index] = message

                    if message.get("error"):
                        finish(
                            RuntimeError(
                                f"codex app-server {msg_id} failed: "
                                f"{json.dumps(message['error'])}"
                            )
                        )
                        return

                    if msg_id == 1:
                        child.stdin.write(
                            json.dumps({"method": "initialized", "params": {}}) + "\n"
                        )
                        child.stdin.flush()
                        for request in requests:
                            send(request["method"], request["params"])
                    elif len(pending) == 0:
                        finish(None)
                        return
        except Exception:
            pass

    def read_stderr():
        try:
            for line in child.stderr:
                stderr_buf.append(line)
        except Exception:
            pass

    stdout_thread = threading.Thread(target=read_stdout, daemon=True)
    stderr_thread = threading.Thread(target=read_stderr, daemon=True)
    stdout_thread.start()
    stderr_thread.start()

    send(
        "initialize",
        {
            "clientInfo": {"name": "ai-workflow-deploy", "version": "1.0.0"},
            "capabilities": {"experimentalApi": True},
        },
    )

    triggered_timeout = not finished.wait(timeout_seconds)
    if triggered_timeout:
        finish(RuntimeError("codex app-server timed out"))

    try:
        exit_code = child.wait(timeout=5)
    except Exception:
        exit_code = None
    if not settled["done"] and exit_code not in (0, None):
        finish(RuntimeError(f"codex app-server exited with {exit_code}"))

    error = settled["error"]
    if error:
        stderr_text = "".join(stderr_buf).strip()
        if stderr_text:
            error.args = (f"{error.args[0]}\n{stderr_text}",)
        raise error
    return results


