def resolve_user_path(value):
    if not value or value == "~":
        return os.path.expanduser("~")
    if value.startswith("~" + os.sep) or value.startswith("~/") or value.startswith("~\\"):
        return os.path.join(os.path.expanduser("~"), value[2:])
    return os.path.abspath(value)


def ensure_deploy_source():
    if not (os.path.exists(source_dir) and os.path.isdir(source_dir)):
        raise RuntimeError(f"Deploy source not found: {source_dir}")


def ensure_dir(directory):
    os.makedirs(directory, exist_ok=True)


def remove_path(target):
    if os.path.islink(target) or os.path.isfile(target):
        try:
            os.remove(target)
        except FileNotFoundError:
            pass
    elif os.path.isdir(target):
        shutil.rmtree(target, ignore_errors=True)


def copy_path(src, dest):
    remove_path(dest)
    ensure_dir(os.path.dirname(dest))
    if os.path.isdir(src):
        shutil.copytree(src, dest)
    else:
        shutil.copy2(src, dest)


def read_json(file):
    with open(file, "r", encoding="utf-8") as handle:
        raw = handle.read().strip()
    return json.loads(raw) if raw else {}


def write_json(file, value):
    ensure_dir(os.path.dirname(file))
    with open(file, "w", encoding="utf-8") as handle:
        handle.write(json.dumps(value, indent=2, ensure_ascii=False) + "\n")


def build_agents_content(target_file_name):
    sections = [
        {"title": "Global Instructions", "file": os.path.join(source_dir, "CLAUDE.md")},
    ]

    rules_dir = os.path.join(source_dir, "rules")
    if os.path.exists(rules_dir) and os.path.isdir(rules_dir):
        files = sorted([f for f in os.listdir(rules_dir) if f.endswith(".md")])
        for file in files:
            if file == "global.md":
                title = "Global Rules"
            else:
                title = f"Global Rules - {os.path.splitext(file)[0].upper()}"
            sections.append({"title": title, "file": os.path.join(rules_dir, file)})

    parts = []
    for section in sections:
        if not (os.path.exists(section["file"]) and os.path.isfile(section["file"])):
            continue
        relative_path = os.path.relpath(section["file"], source_dir).replace("\\", "/")
        with open(section["file"], "r", encoding="utf-8") as handle:
            content = handle.read().strip()
        parts.append(
            f"# {section['title']}\n\n<!-- Source: deploy/{relative_path} -->\n\n{content}"
        )

    body = "\n\n---\n\n".join(parts)

    lower_name = os.path.splitext(os.path.basename(target_file_name))[0].lower()
    return (
        f"<!-- Generated by ai-workflow make sync-system. Do not edit "
        f"~/.{lower_name}/{target_file_name} directly. -->\n\n{body}\n"
    )


